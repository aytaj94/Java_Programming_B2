package day03_comments_escape_sequence;

public class TabAndNextLine {

    public static void main(String[] args) {

        // The following are about \t - tab
        System.out.println("This is not tabbed");System.out.println("\tThis is tabbed 1 time"); // This is tabbed 1 time
        System.out.println("    This is not tabbed");  // 4 space is equal 1 tab
        System.out.println("\t\tThis is tabbed 2 time");
        System.out.println();

        // The following are about \n - tab
        System.out.println(") Go to store1");
        System.out.println("2) grab milk");
        System.out.println("3) pay cashier");


        System.out.println("\n1) Go to store\n2) grab milk\n3) pay cashier");

    }
    // MAC
}
